    public static void convRemotcode(byte[] srcData, byte[] dstData, int dataLen) {
        byte K1, K2, K3, pv;
        int scnt = 0;
        K1 = srcData[0];
        K2 = (byte) (dataLen - 1);
        dstData[0] = K2;

        for (K3 = 1; K3 < dataLen; K3++) {
            pv = (byte) ((pwda[K3] + pwdb[K2] - pwdc[K1]) % 256);
            dstData[K3] = (byte) (srcData[K3] ^ pv);
        }

        if (dstData[3] != 0) {
            scnt++;
            if (scnt % 2 != 0) {
                if (dstData[3] != (byte) 0xFF) {
                    dstData[dstData[3]] = (byte) (dstData[dstData[3]] ^ dstData[4]);
                } else {
                    K1 = dstData[26];
                    K2 = dstData[27];
                    K3 = dstData[28];
                    dstData[26] = dstData[29];
                    dstData[27] = dstData[30];
                    dstData[28] = dstData[31];
                    dstData[29] = dstData[32];
                    dstData[30] = K1;
                    dstData[31] = K2;
                    dstData[32] = K3;
                }
            }
        }
    }

